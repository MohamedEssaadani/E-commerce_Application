    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request)
    {
        //validation
        $request->validate([
            'name' => 'required|string|max:255',
            'email' => 'required|string|email|max:255|unique:users,email,' . auth()->id(),
            'password' => 'sometimes|nullable|string|min:8|confirmed',
        ]);

        $user = auth()->user();
        $input = $request->except('password', 'password_confirmation');

        //in case when user did not fill the password fields
        if (!$request->filled('password')) {
            //use fill function of this user object to populate it with values of input (request values)
            //& save changes
            $user->fill($input)->save();

            return back()->with('success_message', 'Profile Updated Successfully!');
        }

        //in case when user fill the password fields
        //fill in user password with password(request password)
        $user->password = bcrypt($request->password);

        $user->fill($input)->save();

        return back()->with('success_message', 'Profile Updated Successfully!');
    }

